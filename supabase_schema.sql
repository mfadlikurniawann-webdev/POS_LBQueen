-- Supabase / PostgreSQL Schema untuk LBQueen POS

-- Tabel Kategori (Skincare, Obat Minum, Tindakan, dll)
CREATE TABLE categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL
);

-- Tabel Produk (Barang & Obat & Treatment & Aset)
CREATE TABLE products (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_code VARCHAR(50) UNIQUE NOT NULL,
    name VARCHAR(255) NOT NULL,
    category_id BIGINT REFERENCES categories(id) ON DELETE SET NULL,
    type VARCHAR(50) NOT NULL CHECK (type IN ('Treatment Care & Beauty', 'Product Care & Beauty', 'Treatment', 'Retail Produk', 'Barang Kantor', 'Aset Karyawan')),
    purchase_price DECIMAL(10,2) NOT NULL,
    selling_price DECIMAL(10,2) NOT NULL,
    stock INT DEFAULT 0,
    unit VARCHAR(50), -- e.g., Pcs, Botol, Sesi
    image_url TEXT NULL, -- Link ke foto produk/treatment
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabel Customers (Dengan fitur Membership)
CREATE TABLE customers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    phone VARCHAR(50) NULL,
    is_member BOOLEAN DEFAULT FALSE,
    join_date TIMESTAMPTZ DEFAULT NOW(),
    created_at TIMESTAMPTZ DEFAULT NOW(),
    -- Kolom login portal pelanggan
    username VARCHAR(100) UNIQUE NULL,      -- e.g., "sriwahyuni" (dari nama, lowercase, no spasi)
    password_hash TEXT NULL,               -- bcrypt/base64 hash dari password plain
    password_plain VARCHAR(100) NULL       -- untuk tampil di admin: "SW-M-5" format
);

