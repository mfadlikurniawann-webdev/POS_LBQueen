-- Tabel Vouchers Khusus Membership
CREATE TABLE vouchers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(50) UNIQUE NOT NULL,
    name VARCHAR(100) NOT NULL,
    discount_amount DECIMAL(15,2) NOT NULL, -- Potongan Rupiah langsung (Fleksibel)
    min_purchase DECIMAL(15,2) DEFAULT 0,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabel Transaksi Kasir (Header) - Di-update dengan dukungan voucher & member
CREATE TABLE transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_number VARCHAR(50) UNIQUE NOT NULL,
    user_id BIGINT, -- Kasir
    customer_id BIGINT REFERENCES customers(id) ON DELETE SET NULL,
    voucher_id BIGINT REFERENCES vouchers(id) ON DELETE SET NULL,
    subtotal DECIMAL(15,2) NOT NULL,
    discount_applied DECIMAL(15,2) DEFAULT 0,
    total_amount DECIMAL(15,2) NOT NULL,
    payment DECIMAL(15,2) NOT NULL,
    change_amount DECIMAL(15,2) NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabel Detail Transaksi (Item yang dibeli)
CREATE TABLE transaction_details (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id BIGINT REFERENCES transactions(id) ON DELETE CASCADE,
    product_id BIGINT REFERENCES products(id) ON DELETE SET NULL,
    quantity INT NOT NULL,
    price DECIMAL(10,2) NOT NULL,
    subtotal DECIMAL(15,2) NOT NULL
);

-- Tabel Users (Dengan tambahan role jika perlu)
CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    name VARCHAR(100) NOT NULL,
    role VARCHAR(50) NOT NULL CHECK (role IN ('admin', 'kasir', 'owner', 'digital_ops')),
    created_at TIMESTAMPTZ DEFAULT NOW()
);

